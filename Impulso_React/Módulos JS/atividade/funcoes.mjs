function mostraIdade(nome, idade){
    return `A idade de(a) ${nome} é ${idade}`;
}

function mostraCidade(nome, cidade){
    return `A cidade de(a) ${nome} é ${cidade}`;
}

function mostraHobby(nome, hobby){
    return `O hobby de(a) ${nome} é ${hobby}`;
}

export {
    mostraIdade,
    mostraCidade,
    mostraHobby
}